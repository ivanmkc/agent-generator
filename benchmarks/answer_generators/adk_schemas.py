"""Pydantic schemas for Structured ADK Agent outputs."""

from typing import List, Optional, Dict
from pydantic import BaseModel, Field


class RelevantModules(BaseModel):
    """List of fully qualified module paths relevant to the user request."""

    modules: List[str] = Field(
        ...,
        description="List of fully qualified module paths (e.g., 'google.adk.agents.LlmAgent') that are relevant to solving the user request.",
    )


class Plan(BaseModel):
    """The implementation plan generated by the Planner."""

    workspace_dir: str = Field(
        ..., description="The workspace directory for this task."
    )
    user_request: str = Field(..., description="The user request being addressed.")
    steps: List[str] = Field(..., description="Step-by-step plan for implementation.")
    files_to_create: List[str] = Field(
        default_factory=list, description="List of file paths to create."
    )
    files_to_modify: List[str] = Field(
        default_factory=list, description="List of file paths to modify."
    )
    rationale: str = Field(..., description="Reasoning behind the plan.")


class TestCase(BaseModel):
    """A single test case for agent verification."""

    test_prompt: str = Field(
        ..., description="The specific prompt to send to the newly created agent."
    )
    expected_behavior: str = Field(
        ...,
        description="Detailed description of the expected output or behavior (e.g., 'Agent should return a JSON matching schema X').",
    )
    requirement_id: Optional[str] = Field(
        None, description="Optional ID linking this test to a specific requirement."
    )


class VerificationPlan(BaseModel):
    """A comprehensive verification strategy consisting of multiple test cases."""

    rationale: str = Field(
        ..., description="Reasoning behind selecting these specific test cases."
    )
    test_cases: List[TestCase] = Field(
        ...,
        description="List of test cases that completely and comprehensively test the requirements.",
    )


class PlanningResult(BaseModel):
    """Combined implementation and verification plan from the Planner."""

    rationale: str = Field(..., description="Reasoning for the plan.")
    steps: List[str] = Field(..., description="Step-by-step implementation plan.")
    verification_plan: VerificationPlan = Field(
        ..., description="The verification strategy, including the test prompt."
    )


class CandidateSolution(BaseModel):
    """The solution summary from the Candidate Creator."""

    implementation_summary: str = Field(..., description="Summary of changes made.")
    # We don't need 'files' content here because the agent uses 'write_file' tool.
    # This schema is for the final turn output.
    ready_for_verification: bool = Field(
        True, description="Whether the solution is ready to be verified."
    )


class VerificationResult(BaseModel):
    """The result of the verification step."""

    success: bool = Field(..., description="Whether the verification passed.")
    error_message: Optional[str] = Field(
        None, description="Error message if verification failed."
    )
    logs: str = Field(..., description="Output logs from the verification run.")
    analysis: str = Field(..., description="Analysis of the result.")


class FinalResponse(BaseModel):
    """The final answer to the user."""

    code: str = Field(
        ..., description="The final answer or code requested by the user."
    )
    rationale: str = Field(..., description="Explanation of the solution.")


class SetupContext(BaseModel):
    """Context passed from Setup agent to Planner."""

    workspace_dir: str = Field(
        ..., description="The directory created for this task (e.g., 'task_123')."
    )
    user_request: str = Field(..., description="The original user request/prompt.")
    sanitized_user_request: Optional[str] = Field(
        None,
        description="The user's request, with direct tool-calling instructions removed.",
    )
    knowledge_context: Optional[str] = Field(
        None,
        description="Relevant API information or code snippets retrieved from the codebase.",
    )
